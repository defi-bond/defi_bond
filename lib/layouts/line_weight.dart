/// Line Weight
/// ------------------------------------------------------------------------------------------------

class SPDLineWeight {

  /// Defines the line weights/thickness.
  const SPDLineWeight._();

  /// The application's default line weight.
  static const double w1 = 1.5;

  /// Return 2 times the application's default line weight.
  double get w2 => w1 * 2.0;

  /// Return 3 times the application's default line weight.
  double get w3 => w1 * 3.0;

  /// Return 4 times the application's default line weight.
  double get w4 => w1 * 4.0;

  /// Return 5 times the application's default line weight.
  double get w5 => w1 * 5.0;

  /// Return 6 times the application's default line weight.
  double get w6 => w1 * 6.0;

  /// Return 7 times the application's default line weight.
  double get w7 => w1 * 7.0;

  /// Return 8 times the application's default line weight.
  double get w8 => w1 * 8.0;

  /// Return 9 times the application's default line weight.
  double get w9 => w1 * 9.0;

  /// Return 10 times the application's default line weight.
  double get w10 => w1 * 10.0;
}